const humanize = (value) => value.replace(/-/g, " ");

const formatDate = () => {
  const now = new Date();
  const year = now.getFullYear();
  const month = String(now.getMonth() + 1).padStart(2, "0");
  const day = String(now.getDate()).padStart(2, "0");
  return `${year}-${month}-${day}`;
};

const main = async () => {
  const dirEntries = await fs.readdir(collectionsDir, { withFileTypes: true });
  const collectionIds = dirEntries
    .filter((entry) => entry.isFile() && entry.name.endsWith(".json"))
    .map((entry) => entry.name.replace(/\.json$/u, ""))
    .sort();

  const createdAt = formatDate();
  const lines = [
    'import type { EventCollectionMeta } from "../types";',
    "",
    "// Generated by scripts/generate-histography-collections-ts.js",
    "export const HISTOGRAPHY_COLLECTIONS: EventCollectionMeta[] = [",
    ...collectionIds.flatMap((collectionId) => {
      const meta = categoryMeta[collectionId] ?? {
        emoji: "🗂️",
        description: `Histography events grouped under ${humanize(collectionId)}.`,
      };

      return [
        "  {",
        `    id: ${JSON.stringify(collectionId)},`,
        `    name: ${JSON.stringify(humanize(collectionId))},`,
        `    emoji: ${JSON.stringify(meta.emoji)},`,
        `    description: ${JSON.stringify(meta.description)},`,
        '    author: "Histography",',
        `    createdAt: ${JSON.stringify(createdAt)},`,
        "  },",
      ];
    }),
    "];",
    "",
    "export const HISTOGRAPHY_COLLECTION_URLS: Record<string, URL> = {",
    ...collectionIds.map(
      (collectionId) =>
        `  ${JSON.stringify(collectionId)}: new URL(${JSON.stringify(`./collections/histography/${collectionId}.json`)}, import.meta.url),`,
    ),
    "};",
    "",
  ];

  await fs.writeFile(outputFile, lines.join("\n"));
  console.log(`Generated src/data/histographyCollections.ts from ${collectionIds.length} files`);
};

await main();
